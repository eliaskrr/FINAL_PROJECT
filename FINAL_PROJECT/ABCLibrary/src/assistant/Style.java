package assistant;

import java.awt.Color;

/*
 * Contiene los colores que vamos a utilizar en nuestro
 * programa
 */
public class Style {

	private Color redColor = Color.decode("#900B0B");
	private Color yellowColor = Color.decode("#F4F2B5");

	public Color getRedColor() {
		return redColor;
	}

	public Color getYellowColor() {
		return yellowColor;
	}

}
